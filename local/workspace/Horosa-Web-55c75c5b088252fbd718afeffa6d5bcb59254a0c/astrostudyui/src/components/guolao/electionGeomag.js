const A = 6378.137;
const B = 6356.7523142;
const RE = 6371.2;
const MAXORD = 12;

function buildModel(model){
	const snorm = new Array(169).fill(0);
	const c = Array.from({length: 13}, ()=>new Array(13).fill(0));
	const cd = Array.from({length: 13}, ()=>new Array(13).fill(0));
	const k = Array.from({length: 13}, ()=>new Array(13).fill(0));
	const fn = new Array(13).fill(0);
	const fm = new Array(13).fill(0);
	model.rows.forEach((row)=>{
		const [n, m, gnm, hnm, dgnm, dhnm] = row;
		if(m <= n){
			c[m][n] = gnm;
			cd[m][n] = dgnm;
			if(m !== 0){
				c[n][m - 1] = hnm;
				cd[n][m - 1] = dhnm;
			}
		}
	});
	// Schmidt 归一化 → 非归一化 Gauss 系数(照 Geomag.java E0000 mode 0 循环逐行)
	snorm[0] = 1.0;
	let j;
	for(let n = 1; n <= MAXORD; n++){
		snorm[n] = snorm[n - 1] * (2 * n - 1) / n;
		j = 2;
		for(let m = 0; m <= n; m++){
			k[m][n] = (((n - 1) * (n - 1)) - (m * m)) / ((2 * n - 1) * (2 * n - 3));
			if(m > 0){
				const flnmj = ((n - m + 1) * j) / (n + m);
				snorm[n + m * 13] = snorm[n + (m - 1) * 13] * Math.sqrt(flnmj);
				j = 1;
				c[n][m - 1] = snorm[n + m * 13] * c[n][m - 1];
				cd[n][m - 1] = snorm[n + m * 13] * cd[n][m - 1];
			}
			c[m][n] = snorm[n + m * 13] * c[m][n];
			cd[m][n] = snorm[n + m * 13] * cd[m][n];
		}
		fn[n] = n + 1;
		fm[n] = n;
	}
	k[1][1] = 0.0;
	return { epoch: model.epoch, c, cd, k, fn, fm };
}

const BUILT = WMM_MODELS.map(buildModel);
const MIN_YEAR = Math.min(...WMM_MODELS.map((m)=>m.epoch)) - 5;
const MAX_YEAR = Math.max(...WMM_MODELS.map((m)=>m.epoch)) + 10;

export const WMM_VALID_RANGE = { min: MIN_YEAR, max: MAX_YEAR };

// year 十进制年(y + (month-1)/12,照 Geomag.setDate);lon 东经+,lat 北纬+;alt 千米(默认 0)。
// 返回 { declination(度,东偏+), outOfRange, clampedYear }。
export function getMagneticDeclination(yearInput, lonDeg, latDeg, altKm = 0){
	let year = Number(yearInput);
	let outOfRange = false;
	if(!Number.isFinite(year)){
		return { declination: 0, outOfRange: true, clampedYear: MIN_YEAR };
	}
	if(year < MIN_YEAR){ year = MIN_YEAR; outOfRange = true; }
	if(year > MAX_YEAR){ year = MAX_YEAR; outOfRange = true; }
	let mdl = BUILT[0];
	for(let i = BUILT.length - 1; i >= 0; i--){
		if(Math.floor(year) >= BUILT[i].epoch){ mdl = BUILT[i]; break; }
	}
	const { c, cd, k, fn, fm, epoch } = mdl;
	const dt = year - epoch;
	const dtr = Math.PI / 180.0;
	const glat = Number(latDeg) || 0;
	const glon = Number(lonDeg) || 0;
	const alt = Number(altKm) || 0;
	const rlon = glon * dtr;
	const rlat = glat * dtr;
	const srlon = Math.sin(rlon);
	const srlat = Math.sin(rlat);
	const crlon = Math.cos(rlon);
	const crlat = Math.cos(rlat);
	const srlat2 = srlat * srlat;
	const crlat2 = crlat * crlat;
	const sp = new Array(13).fill(0);
	const cp = new Array(13).fill(0);
	const pp = new Array(13).fill(0);
	const dp = Array.from({length: 13}, ()=>new Array(13).fill(0));
	const tc = Array.from({length: 13}, ()=>new Array(13).fill(0));
	const snorm = new Array(169).fill(0);
	sp[0] = 0.0;
	cp[0] = 1.0;
	snorm[0] = 1.0;
	pp[0] = 1.0;
	dp[0][0] = 0.0;
	sp[1] = srlon;
	cp[1] = crlon;
	const a2 = A * A, b2 = B * B, c2 = a2 - b2, a4 = a2 * a2, b4 = b2 * b2, c4 = a4 - b4;
	// 大地坐标 → 球坐标
	const q = Math.sqrt(a2 - c2 * srlat2);
	const q1 = alt * q;
	const q2 = ((q1 + a2) / (q1 + b2)) * ((q1 + a2) / (q1 + b2));
	const ct = srlat / Math.sqrt(q2 * crlat2 + srlat2);
	const st = Math.sqrt(1.0 - ct * ct);
	const r2 = (alt * alt) + 2.0 * q1 + (a4 - c4 * srlat2) / (q * q);
	const r = Math.sqrt(r2);
	const d = Math.sqrt(a2 * crlat2 + b2 * srlat2);
	const ca = (alt + d) / r;
	const sa = c2 * crlat * srlat / (r * d);
	for(let m = 2; m <= MAXORD; m++){
		sp[m] = sp[1] * cp[m - 1] + cp[1] * sp[m - 1];
		cp[m] = cp[1] * cp[m - 1] - sp[1] * sp[m - 1];
	}
	const aor = RE / r;
	let ar = aor * aor;
	let br = 0.0, bt = 0.0, bp = 0.0, bpp = 0.0;
	for(let n = 1; n <= MAXORD; n++){
		ar = ar * aor;
		for(let m = 0; m <= n; m++){
			// 非归一化连带勒让德多项式与导数(递推,照原文三分支)
			if(n === m){
				snorm[n + m * 13] = st * snorm[n - 1 + (m - 1) * 13];
				dp[m][n] = st * dp[m - 1][n - 1] + ct * snorm[n - 1 + (m - 1) * 13];
			}else if(n === 1 && m === 0){
				snorm[n + m * 13] = ct * snorm[n - 1 + m * 13];
				dp[m][n] = ct * dp[m][n - 1] - st * snorm[n - 1 + m * 13];
			}else if(n > 1 && n !== m){
				if(m > n - 2){ snorm[n - 2 + m * 13] = 0.0; }
				if(m > n - 2){ dp[m][n - 2] = 0.0; }
				snorm[n + m * 13] = ct * snorm[n - 1 + m * 13] - k[m][n] * snorm[n - 2 + m * 13];
				dp[m][n] = ct * dp[m][n - 1] - st * snorm[n - 1 + m * 13] - k[m][n] * dp[m][n - 2];
			}
			// 时间修正 Gauss 系数
			tc[m][n] = c[m][n] + dt * cd[m][n];
			if(m !== 0){
				tc[n][m - 1] = c[n][m - 1] + dt * cd[n][m - 1];
			}
			// 球谐求和
			const par = ar * snorm[n + m * 13];
			let temp1, temp2;
			if(m === 0){
				temp1 = tc[m][n] * cp[m];
				temp2 = tc[m][n] * sp[m];
			}else{
				temp1 = tc[m][n] * cp[m] + tc[n][m - 1] * sp[m];
				temp2 = tc[m][n] * sp[m] - tc[n][m - 1] * cp[m];
			}
			bt = bt - ar * temp1 * dp[m][n];
			bp += fm[m] * temp2 * par;
			br += fn[n] * temp1 * par;
			// 地理极点特例
			if(st === 0.0 && m === 1){
				if(n === 1){ pp[n] = pp[n - 1]; }
				else { pp[n] = ct * pp[n - 1] - k[m][n] * pp[n - 2]; }
				const parp = ar * pp[n];
				bpp += fm[m] * temp2 * parp;
			}
		}
	}
	if(st === 0.0){ bp = bpp; } else { bp /= st; }
	const bx = -bt * ca - br * sa;
	const by = bp;
	const dec = Math.atan2(by, bx) / dtr;
	return {
		declination: Number.isFinite(dec) ? dec : 0,
		outOfRange,
		clampedYear: year,
	};
}

// DMS 输入解析:「5W30'00」「0E00'00」「-4.5」等 → 十进制度(东偏+)。照 Moira 磁北度数输入语义。
export function parseMagneticDms(text){
	const raw = `${text === undefined || text === null ? '' : text}`.trim();
	if(!raw){
		return null;
	}
	const plain = Number(raw);
	if(Number.isFinite(plain)){
		return plain;
	}
	const m = raw.toUpperCase().match(/^(\d{1,3})\s*([EW])\s*(\d{1,2})?(?:'|′)?\s*(\d{1,2})?/);
	if(!m){
		return null;
	}
	const deg = Number(m[1]) + (Number(m[3] || 0) / 60) + (Number(m[4] || 0) / 3600);
	return m[2] === 'W' ? -deg : deg;
}

export function formatMagneticDms(dec){
	const val = Number(dec) || 0;
	const dir = val >= 0 ? 'E' : 'W';
	const abs = Math.abs(val);
	const d = Math.floor(abs);
	const mFloat = (abs - d) * 60;
	const mm = Math.floor(mFloat);
	const ss = Math.round((mFloat - mm) * 60);
	return `${d}${dir}${String(mm).padStart(2, '0')}'${String(ss).padStart(2, '0')}`;
}
